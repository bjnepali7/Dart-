void main() {
  List<int> id = [1, 3, 4, 5, 6, 7, 6];
  // for in loop is a loop that iterates over a collection of items, such as a list or an array.
  // it is used to perform an action on each item in the collection, such as printing the item or performing a calculation on it.
  // it is  different from for and for each loop because it is more concise and easier to read when iterating over a collection of items.

  for (int a in id) {
    print(a);
  }
}

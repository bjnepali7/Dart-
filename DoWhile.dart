void main() {
  //do while loop
  // do while loop is a loop that executes a block of code at least once and then repeats the loop as long as a specified condition is true.

  // the syntax of do while loop is: do{//code to be executed} while(condition);
  // it is a exit control loop

  String val = "bijay";

  int i = 0;
  do {
    print(val[i]);
    i++;
  } while (i != i);
}
